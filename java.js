$(document).ready(function(){
	$(".teamskills").hide();
	$(".empathic").hide();
	$(".interface3").hide();
	$(".interface3").hide();

	$("#btnsoftskills").click(function(){
		$(".teamskills").show();
		$(".empathic").show();
		$(".interface3").show();
		$(".interface3").show();
	});

	$("#btnhardskills").click(function(){
		$(".teamskills").hide();
		$(".empathic").hide();
		$(".interface3").hide();
		$(".interface3").hide();
	});

});